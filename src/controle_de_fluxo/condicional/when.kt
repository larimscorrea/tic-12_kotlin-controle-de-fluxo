package controle_de_fluxo.condicional

fun main() {
    val dia = "Segunda"

    when (dia) {
        "Segunda" -> println("Segunda-feira")
        "Terça" -> println("Terça-feira")
        "Quarta" -> println("Quarta-feira")
        "Quinta" -> println("Quinta-feira")
        "Sexta" -> println("Sexta-feira")
        "Sábado" -> println("Sábado")
        "Domingo" -> println("Domingo")
    }

}

